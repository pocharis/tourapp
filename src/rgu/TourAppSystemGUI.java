    private void btnCreateBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateBookingActionPerformed
        try {
            // creating a booking:
            Tourist tR;
            TourPackage tP;
            Booking booking;
            
            
            int index = this.comboTourist.getSelectedIndex();
            tR = this.touristList.get(index);
            
            int index2 = this.comboTourPackage.getSelectedIndex();
            tP = this.tourPackList.get(index2);
            
            
            String dateTime = this.txtDateTime.getText();
            
            //creating a new booking and adding to the booking list
            SimpleDateFormat format = new SimpleDateFormat("dd-MMM-yyyy HH:mm:ss");
            Date bookingDate = format.parse(dateTime);
            booking =  new Booking(tR, tP, bookingDate);
            
            bookingList.add(booking);
            updateComboBooking();
            JOptionPane.showMessageDialog(null, "Booking For " + tR.getName() + " in and around " + tR.getCountry() + " successfully created");
        } catch (ParseException ex) {
//            Logger.getLogger(TourAppSystemGUI.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(new JFrame(), "Please Enter Date in this format: 31-Dec-2020 23:37:50", "Alert", JOptionPane.WARNING_MESSAGE);
                    
        }
    }//GEN-LAST:event_btnCreateBookingActionPerformed

