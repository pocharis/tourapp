    private void comboBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBookingActionPerformed
        // get the details of the selected booking
        int indexBookingDetail = this.comboBooking.getSelectedIndex();
        Booking bp = this.bookingList.get(indexBookingDetail);
        
        
        //get the name and country of tourist
        this.txtName.setText(bp.getTourist().getName());
        this.txtCountry.setText(bp.getTourist().getCountry());
        

        //populating the various textboxes
        int indexLangEnum = AudioLanguage.valueOf(bp.getTourist().getAudioLanguage().name()).ordinal();
        this.comboLanguage.setSelectedIndex(indexLangEnum);        
        this.txtDescription.setText(bp.getTourPackage().getDescription());
        this.txtLocation.setText(bp.getTourPackage().getLocation());
        this.txtTourCost.setText(String.valueOf(bp.getTourPackage().getTourCost()));
        
        
        //checking if tourpackage is cityTout, to be able to populate that section
        if(bp.getTourPackage().getClass() == CityTour.class){
            JOptionPane.showMessageDialog(null, "CityTour selected");
            
            this.radioCityTour.doClick();
            double costGuide = bp.getTourPackage().getCost() - bp.getTourPackage().getTourCost();
            this.txtCostForGuide.setText(String.valueOf(costGuide));
            
            if (costGuide == 0.0)this.radioNo.doClick();
            if (costGuide > 0.0)this.radioYes.doClick();
        }
        
        //checking if tourpackage is sightseeing, to be able to populate that section
        if(bp.getTourPackage().getClass() == SightSeeingTour.class){
            JOptionPane.showMessageDialog(null, "SightSeeingTour");
            this.radioSightSeeing.doClick();
            double costTicket = bp.getTourPackage().getCost() - bp.getTourPackage().getTourCost();
            this.txtCostForTickets.setText(String.valueOf(costTicket));
            this.txtCostForGuide.setText("");
            this.buttonGroup2.clearSelection();
        }
        
    }//GEN-LAST:event_comboBookingActionPerformed

