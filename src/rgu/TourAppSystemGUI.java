    private void btnSaveTouristActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveTouristActionPerformed
        // save the tourist data
        String name = this.txtName.getText();
        String country = this.txtCountry.getText();
        
        if(name.isEmpty()){
             JOptionPane.showMessageDialog(null, "Please enter a name for tourist");
        }else if(country.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a country for tourist");
        }else{
            
            //creating a tourist and updating the tourist list
            String lang = this.comboLanguage.getSelectedItem().toString();
            Tourist tr = new Tourist(name, country, AudioLanguage.valueOf(lang));
            touristList.add(tr);
            updateTouristCombo();
            JOptionPane.showMessageDialog(null, name + " from " + country + " speaking " + lang + ", successfully added, check tourist list");
            
            //optionally clearing the textbox for a new entry
            this.txtName.setText("");
            this.txtCountry.setText("");
        }
        
    }//GEN-LAST:event_btnSaveTouristActionPerformed

    private void btnViewBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewBillActionPerformed
        // View the bill of the selected tourist
        if(this.comboBooking.getItemCount() != 0){
            Booking bK;
            BookingUtil b = new BookingUtil();

            int index = this.comboBooking.getSelectedIndex();
            bK = bookingList.get(index);

            //getting tourist name and calculating the bill
            String name = bK.getTourist().getName();
            Double total = b.calculateBill(bookingList, bK.getTourist()).get(0);
            Double discountedTotal = b.calculateBill(bookingList, bK.getTourist()).get(1);

            //displays a well formatted bill for the tourist
            JOptionPane.showMessageDialog(null, name + "'s total bill is " + total + "£ and after discounts it is " + discountedTotal + "£");
        
         }else{
            JOptionPane.showMessageDialog(null, "No booking created yet");
        }
        
        
    }//GEN-LAST:event_btnViewBillActionPerformed

