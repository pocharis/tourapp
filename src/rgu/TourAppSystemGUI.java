    private void btnSaveTourPackageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveTourPackageActionPerformed
        // Saving a tourpackage
        
        //obtaining the input of the textboxes
        String description = this.txtDescription.getText();
        String location = this.txtLocation.getText();
        String tourCostStr = this.txtTourCost.getText(); 
        String guideCostStr = this.txtCostForGuide.getText();
        String ticketCostStr = this.txtCostForTickets.getText();
        
        
        //carrying out validations to make sure the textboxes are not empty
        if(description.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a tour description");
        }else if(location.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a location");
        }else if(tourCostStr.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a tour cost");
        }else{
            //catching errors that may occur when submitting empty textboxes or entering an letters where there should be numbers
            try{
              double tourCost = Double.parseDouble(tourCostStr);
              
              if(this.radioCityTour.isSelected()){
                  
                  try{
                        double guideCost = Double.parseDouble(guideCostStr);
                        if(this.radioNo.isSelected()){
                            CityTour cT = new CityTour(false, guideCost, description, location, tourCost);
                            tourPackList.add(cT);
                            updateTourPackList();
                        }else if(this.radioYes.isSelected()){
                            CityTour cT = new CityTour(true, guideCost, description, location, tourCost);
                            tourPackList.add(cT);
                            updateTourPackList(); 
                        }else{
                            JOptionPane.showMessageDialog(null, "Please selsect Yes or No for with guide option");
                        }
                    }catch(Exception e){
                          JOptionPane.showMessageDialog(null, "Please enter a number for the guide cost");
                    }   
                      
              }else if(this.radioSightSeeing.isSelected()){
                  try{
                        double ticketCost = Double.parseDouble(ticketCostStr);
                        
                        SightSeeingTour sT = new SightSeeingTour(ticketCost, description, location, tourCost);
                        tourPackList.add(sT);
                        updateTourPackList();
                        
                    }catch(Exception e){
                          JOptionPane.showMessageDialog(null, "Please enter a number for the Ticket cost");
                    }   
              }
                
            }catch(Exception e) {
                JOptionPane.showMessageDialog(null, "Please enter a number for the tour cost");
            
            }
        }
        
       
        
      
        
        
        
       
            
        
        
        
        
        
    }//GEN-LAST:event_btnSaveTourPackageActionPerformed

   
