    private void comboAgentListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboAgentListActionPerformed
        
        //on selecting an agent from the list, the form is populated
        TourAgent tA;
        int index = this.comboAgentList.getSelectedIndex();
        tA = this.agentList.get(index);
        this.txtName.setText(tA.getName());
        this.txtLocation.setText(tA.getLocation());
    }//GEN-LAST:event_comboAgentListActionPerformed

