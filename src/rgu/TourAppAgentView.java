    private void btnSaveTourAgentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveTourAgentActionPerformed
        // saving tour agents
        String name = this.txtName.getText();
        String location = this.txtLocation.getText();
        
        //checking for empty text fields
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter an Agents name");
        }else if(location.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter a location");
        }else{
            agentList.add(new TourAgent(name, location));
            updateAgentList();
            JOptionPane.showMessageDialog(null, "Agent: " + name + " in " + location  + " added successfully");
        }
        
        // Clear the textfields after adding an agents name
        this.txtName.setText("");
        this.txtLocation.setText("");
    }//GEN-LAST:event_btnSaveTourAgentActionPerformed

